function runSearch() {
  inquirer
    .prompt({
      name: "action",
      type: "list",
      message: "What would you like to do?",
      choices: [
        "Add employee",
        "Remove employee",
        "Update employee role",
        "View all roles",
        "Add role",
        "Remove roll",
        "Exit",
      ],
    })
    .then(function (answer) {
      switch (answer.action) {
        case "Add employee":
          addEmployee();
          break;

        case "Remove employee":
          removeEmployee();
          break;

        case "Update employee role":
          updateEmployee();
          break;

        case "View all roles":
          viewRoles();
          break;

        case "Add role":
          addRoles();
          break;

        case "Remove role":
          removeRole();
          break;

        case "exit":
          connection.end();
          break;
      }
    });
}

